#include <bits/stdc++.h>
using namespace std;

void solve()
{
    int n;
    cin >> n;
    int x, result = 0;
    for (int i = 0; i < n; i++)
    {
        cin >> x;
        result ^= x;
    }
    cout << result << "\n";
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }   
    return 0;
}